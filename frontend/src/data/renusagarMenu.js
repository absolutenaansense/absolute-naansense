// Build the same shape menuApi.getMenu() returns: { categories: [{ id, name, menuItems }] }.
// Each item gets a stable id and `external: true` so the cart/order layer knows it
// is not a database MenuItem (orders store it as an open item: itemName + price).
function buildMenu() {
  const slug = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '')
  const categories = CATEGORIES.map((cat, ci) => ({
    id: `rs-cat-${ci}`,
    name: cat.name,
    menuItems: cat.items.map((it, ii) => ({
      id: `rs-${ci}-${ii}-${slug(it.name)}`,
      name: it.name,
      description: null,
      price: it.price,
      isVeg: it.isVeg,
      isAvailable: true,
      external: true,
    })),
  }))
  return { categories }
}

export const renusagarMenu = buildMenu()
